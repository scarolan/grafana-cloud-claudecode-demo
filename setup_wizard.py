def setup_mcp_server(config, service_token):
    """Add the Grafana MCP server to Claude"""
    print("\n[MCP] Setting up Claude MCP integration...")

    cmd = [
        'claude', 'mcp', 'add', '-t', 'stdio',
        '-e', f'GRAFANA_URL={config["grafana_url"]}',
        '-e', f'GRAFANA_API_KEY={service_token}',
        'grafana', '--', 'mcp-grafana', '--transport', 'stdio'
    ]

    try:
        result = subprocess.run(cmd, capture_output=True, text=True)
        if result.returncode == 0:
            print("[+] MCP server added successfully!")
            return True
        else:
            print(f"[!] MCP setup failed: {result.stderr}")
            return False
    except Exception as e:
        print(f"[!] MCP setup failed: {e}")
        return False

def install_packages():
    """Install required Python packages"""
    print("\n[PKG] Installing required packages...")

    packages = [
        'mcp-grafana',
        'opentelemetry-distro',
        'opentelemetry-exporter-otlp'
    ]

    for package in packages:
        print(f"   Installing {package}...")
        result = subprocess.run([sys.executable, '-m', 'pip', 'install', package],
                               capture_output=True, text=True)
        if result.returncode != 0:
            print(f"[!] Failed to install {package}")
            return False

    print("[+] All packages installed!")
    return True

